#pragma once

// Telemetry Registers (3)
#define CELL0 0x00
#define CELL1 0x01
#define CELL2 0x02

// Movement Registers (7)
#define XDIR0 0x03
#define XDIR1 0X04
#define YDIR0 0x05
#define YDIR1 0X06
#define WDIR0 0x07
#define WDIR1 0x08
#define MOVET 0x09

// Sound Registers (5)
#define SOUND 0x0A
#define FREQ0 0x0B
#define DUTY0 0x0C
#define FREQ1 0x0D
#define DUTY1 0x0E

// Lighting Registers (2)
#define LIGH0 0x0F
#define LIGH1 0x10